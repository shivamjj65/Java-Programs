package com.programs;

public class P02_Literals {
    public static void main(String[] args) {
        System.out.println("A literal is a constant value assigned to a variable");
        int a = 20;
        float b = 20.33f;
        double c = 30.34D;
        long d = 988196128L;
        char e = 'A';
        String str = "SHIVAM";
        System.out.print("int is assigned directly, float with f, double with D, long with L, char with ' ', str with double quotes ");

    }
}
